md"""
# BabyAI environment overview
- Dynamics and Action Space:
  - Agent navigates $3\times 3$ gridworld (this is adjustable).
  - Agent uses tank controls: left, right, forward, pick-up,  put-down.
  - Agent may only carry one item at a time.
- Obsevation Space:
  - top down view of gridworld with channels encoding
    - object type: box, ball, or key (renamed to phone for Synonyms Environment)
    - color
  - Direction agent is facing (encoded as an integer)
  - Mission: encoded as integer vector using GPT tokenizer
- Reward function:
  - 1 for successful completion of mission
  - 0 all other time steps
- Termination function:
  - Upon successful completion of mission
  - Upon expiration of time-limit (dynamically computed to ensure possibility of success)
  - If agent picks up wrong object on pick-up task
"""

# ╔═╡ dfec2144-f4cf-4fc5-9958-26e7e8112246
md"""
# Architecture

- In parallel:
  - (a) Feed top-down view of gridworld through a CNN
    - output-size: 32
    - kernel-size: 3
    - stride: 2
  - (b) Encode direction agent is facing as one-hot
  - (c) Encode mission:
    - For PPO baseline: use bag-of-words embedding
    - For GPT architecture: encode instruction with GPT-2
- Concatenate (a), (b), and (c) and feed through
    - Linear layer 
      - output-size is the `hidden_size` hyperparameter, usually 256 or 512
    - ReLU
    - In parallel:
      - Project to action distribution with linear layer
      - Project to value estimate with linear layer
"""

