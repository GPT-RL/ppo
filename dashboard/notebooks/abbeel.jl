md"""
# Overview
In this report, we reproduce some of the results in [Pretrained Transformers As
Universal Computation Engines (Gunel et al.)](https://arxiv.org/pdf/2103.05247.pdf) on two datasets, MNIST and XOR. This report focuses especially on comparisons between architectures using a GPT-2 architecture with pretrained, frozen parameters and those with randomly initialized, frozen parameters. We view these comparisons as an indication of the extent to which the model is accessing and taking advantage of the information baked into the pretrained GPT-2 parameters. 
"""

# ╔═╡ 4d322ed3-0fea-42cb-ba29-798c952f4bfd
md"""
# Bottom Line Up Front
- Training **layer norm** is critical to improving performance of Pretrained over Random.
- Training **positional embeddings** usually improves absolute performance but can hurt relative performance of Pretrained with respect to Random.
- Training too many parameters can eliminate the advantage of Pretrained over Random.
- Simply optimizing performance of Pretrained will often *hurt* its performance relative to Random.
"""

# ╔═╡ 43c4761b-df3d-4edb-b925-6eca8da42a4e
md"""
# Architectures
Unless otherwise specified, models default to the **Single Layer Architecture**. GPT-size defaults to "small" as in Gunel et al.

### Single Layer Architecture
This architecture reproduces the one described in Gunel et al.

- Convolutional Layer
  - MNIST: kernel-size of $4\times4$ with stride of $4$.
  - XOR: kernel-size of $1$ with stride of $1$.
- GPT-2 architecture.
- Linear output layer, projecting last hidden state produced by GPT-2 to number of classes.

### Double Layer Architecture
This architecture adds an additional layer to Single Layer Architecture:

- Convolutional Layer
  - MNIST: **kernel-size of $3\times3$ with stride of $1$**.
  - XOR: kernel-size of $1$ with stride of $1$.
- **Convolutional Layer**
  - **MNIST: kernel-size of $4\times4$ with stride of $4$.**
  - **XOR: kernel-size of $1$ with stride of $1$.**
- GPT-2 architecture.
- Linear output layer, projecting last hidden state produced by GPT-2 to number of classes.

### Dense Architecture

- **Flatten observation**
- **Dense Linear Layer**
- GPT-2 architecture
- Linear output layer, projecting last hidden state produced by GPT-2 to number of classes.

# Terminology and Graphics for Subsequent Sections
- We describe the GPT-2 model with pretrained weights as "Pretrained" and the model with randomly initialized weights as "Random." 
- The horizontal purple line in each graph denotes the results reported in (Gunel et al.).
- Text generally refers to subsequent graphs (as opposed to those preceding it).
"""


