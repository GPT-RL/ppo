function run_query(query::String, variables::Dict{String, Any}, key::String)
	HASURA_ENDPOINT_URL = "http://rldl12.eecs.umich.edu:1200/v1/graphql"
	@chain query begin
		HTTP.request(
			"POST",
			HASURA_ENDPOINT_URL;
			verbose=3,
			body= JSON3.write(Dict(
					"query" => _, 
					"variables" => variables
				))
			)
		_.body
		String
		JSON3.read(_, Dict)
		_["data"]
		_[key]
		map(d -> Dict(
				"run_id" => d["run_id"],
				"sweep_id" => d["run"]["sweep_id"],
				d["log"]...,
				d["run"]["metadata"]["parameters"]...
				), _)

		map(d -> Dict(
				d...,
				"action_hidden_size" => if isnothing(get(d, "action_hidden_size", 0)) 
					0 
				else 
					get(d, "action_hidden_size", nothing) 
				end, 
				[k => v for (k1, v1, k2, v2) in [
							(
								"hours", get(d, "time-delta", 0) / 3600, 
								"time-delta", get(d, "hours", 0) * 3600,
							),
							(
								"env", get(d, "env_name", nothing),
								"env_name", get(d, "env", nothing),
							)
						] 
						for (k, v) in [
								(k1, get(d, k1, v1)), 
								(k2, get(d, k2, v2)),
								]]...,				
				[name => get(d, name, false) for name in [
							"randomize_parameters"
						]]...,
				"one_layer" => get(d, "one_layer", true),
				[name => get(d, name, nothing) for name in [
							"test accuracy",
							"test loss",
							"train loss",
						]]... 
				), _)
			vcat(DataFrame.(_)...)
	end
end

