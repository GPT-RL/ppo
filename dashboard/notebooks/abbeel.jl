function plot_df(df::DataFrame; title_keywords::Vector{String}=String["dataset", "lr"])
	dataset = first(df)[:dataset]
	yintercept = Dict("mnist" => 98.0, "xor" => 100.0)[dataset]
	title = join(["$(keyword): $(first(df)[keyword])" for keyword in title_keywords], ", ")
	function _plot(; kwargs...)
		@chain df begin
			Gadfly.with_theme(:default) do
				Gadfly.plot(
					_,
					x="epoch", y="test accuracy", 
					ymin="ymin", ymax="ymax",
					yintercept=[yintercept],
					# linestyle="pretrained",
					Scale.color_discrete(preserve_order=false),
					Guide.title(title),
					Geom.subplot_grid(
						Geom.line, 
						Geom.ribbon, 			
						Geom.hline(style=:dot,color="#b88fff")
					);
					kwargs...,

				)# |> HTMLDocument
			end
		end
	end
	_plot
end

