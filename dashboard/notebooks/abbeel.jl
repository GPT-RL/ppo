# ╔═╡ abaac768-ddc7-4cba-be19-68e20a314826
md"""
# Comparison with Dense Architecture

Reviewing the experiments in Gunel et al., we noticed that patches and strides were always designed to be non-overlapping. We conjecture that the motivation for this decision was to force the GPT-2 architecture to be entirely reponsible for all aggregation across patches. That is, the newly trained parameters are responsible for deveoping a representation of each patch, but never for sharing information *between* patches (except perhaps at the output layer). This architecture tests the effects of permitting the learned parameters to shoulder the burden of both representation *and* sharing.

The Dense Architecture replaces the $4 \times 4 \times 768$ kernel of the Single Layer model with a $784 \times 768$ linear layer (these numbers are different for XOR), which receives a flattened version of the 3d digital image. A confounding factor is the addition of many more parameters. We plan to address this by rerunning the single-layer experiment with overlapping strides.

As the results indicate, the Dense architecture learns the task much more easily and the Pretrained model loses its advantage over Random. We used the lower learning rate from Gunel et al. because otherwise both Pretrained and Random learned so quickly that the curves were indistinguishable.
"""

