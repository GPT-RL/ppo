function query(sweep_ids::Vector{Int})
	run_query("""
		query getSweepRuns(\$ids: [Int!]) {
		  run_log(where: {run: {sweep_id: {_in: \$ids}}}) {
			log
			run_id
			run {
			  metadata
			  sweep_id
			}
		  }
		}
  	""", Dict{String, Any}("ids" => sweep_ids), "run_log"
	)
end

# ╔═╡ c8d9a37f-3884-4708-844d-39f36b917ace
function mean_and_std(
		df::DataFrame; 
		group_keys::Vector{String}, 
		keep_keys::Vector{String},
		stat_key::String)
	@chain df begin
		groupby(_, group_keys)
		combine(_, [
				stat_key => mean => stat_key, 
				stat_key => std => "$(stat_key)_std",
				[
					name => first => name
					for name in keep_keys
				]...
				])
		DataFrames.transform(_, [stat_key, "$(stat_key)_std"] => (
			(means, stds) -> 
				[map((m,s)-> [m - s, m + s], means, stds)...]
		) => ["ymin", "ymax"])
	end
end

# ╔═╡ 2d0c631e-2871-4b14-9a32-11fb02cf1dd2
function get_data(sweep_ids::Vector{Int64}; 
		keys = [
		"randomize_parameters",
		"train_wpe",
		"train_ln",
		"gpt_size",
		"one_layer",
		"dataset",
		"lr",
		"batch_size",
		"architecture",
		"parameters",
		"train layer norm",
	]
	)
	mnist0 = @chain query(sweep_ids) begin
		filter(row -> !isnothing(row["test accuracy"]), _)
		ifelse(
			"architecture" in names(_), 
			_, 
			hcat(
				_, 
				DataFrame(
					"architecture" => repeat([nothing], nrow(_))
				),
				makeunique=true
			)
		)
		ifelse(
			"train_ln_1" in names(_), 
			_, 
			hcat(
				_, 
				DataFrame(
					"train_ln_1" => repeat([nothing], nrow(_))
				),
				makeunique=true
			)
		)
		transform(
			_, ["one_layer", "architecture"] => (
				(one_layer, architecture) -> map(
					(
						(one_layer, architecture) -> 
							@match (one_layer, architecture) begin
								(x, "1linear") => "Dense"
								(x, "1Conv") => "Single Layer"
								(x, "2Conv") => "Double Layer"
								(true, nothing) => "Single Layer"
								(false, nothing) => "Double Layer"
								x => throw(DomainError())
							end
					), one_layer, architecture
				)
			) => "architecture"
		)
		transform(
			_, ["train_ln", "train_ln_1"] => (
				(train_ln, train_ln_1) -> map(
					(
						(train_ln, train_ln_1) -> 
							@match (train_ln, train_ln_1) begin
								(true, _) => "all"
								(_, true) => "first"
								(false, _) => "none"
								x => throw(DomainError())
							end
					), train_ln, train_ln_1
				)
			) => "train layer norm"
		)
		transform(
			_, ["lr", "batch_size"] => (
				(lrs, batch_sizes) -> map(
					(
						(lr, batch_size) -> 
						"lr=$(convert(Float64, lr)), batch_size=$batch_size"
					), lrs, batch_sizes
				)
			) => "parameters"
		)
		mean_and_std(
			_, 
			group_keys=[keys..., "epoch"],
			keep_keys=keys,
			stat_key="test accuracy"
		)
		transform(_, "randomize_parameters" => (x -> .!x) => "pretrained")
		rename(
			_, 
			"train_wpe" => "train positional embeddings", 
			"one_layer" => "single layer architecture",
			"gpt_size" => "GPT size"
		)
	end
end

