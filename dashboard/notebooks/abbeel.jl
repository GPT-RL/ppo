#
Note that training positional embeddings yields a marked advantage in all cases except when also training positional embeddings on the Pretrained model. We propose the following explanation for this result. 

Positional information is very important for the XOR task since the model must learn to associate the bit in position-0 with the bit in position-5, the bit in position-1 with the bit in position-6, etc. Since the convolutions have a kernel-size of $1$, they do not overlap, and therefore information can only be aggregated across positions using the GPT-2 architecture. 

If GPT-2 uses randomized parameters, the aggregation procedure will be insensitive to the inputs and all relevant positional information must be represented in the positional embeddings. This explains the poor performance of the Random models that do not train positional embeddings (first column graphs). 

Meanwhile the Pretrained architecture that does not train positional embedings must fall back on transfer from natural-language pretraining for its representation of positional information. Thus the graphs on the right are proof of some kind of transfer. Moreover the graph on the upper right indicates that positional information transferred from pretrainined is comparable to if not slightly better than positional information learned at training time.
